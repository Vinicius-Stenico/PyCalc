import math

def somar(n1, n2):
    resultado = n1 + n2
    return resultado

def subtrair(n1, n2):
    resultado = n1 - n2
    return resultado

def dividir(n1, n2):
    resultado = n1 / n2
    return resultado

def multiplicar(n1, n2):
    resultado = n1 * n2
    return resultado

def potencia(n1):
    resultado = n1 ** 2
    return resultado

def porcentagem(n1, n2):
    resultado = (n2 * n1) / 100
    return resultado

def raiz(n1):
    resultado = math.sqrt(n1)
    return resultado

def fracao(n1):
    resultado = 1 / n1
    return resultado